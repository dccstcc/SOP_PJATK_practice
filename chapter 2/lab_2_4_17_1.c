#include <stdio.h>

int main(void) {

    int dayOfWeek = 7;
    if(dayOfWeek==1) puts("The day of the week is: Monday");
    if(dayOfWeek==2) puts("The day of the week is: Tuesday");
    if(dayOfWeek==3) puts("The day of the week is: Wednesday");
    if(dayOfWeek==4) puts("The day of the week is: Thursday");
    if(dayOfWeek==5) puts("The day of the week is: Friday");
    if(dayOfWeek==6) puts("The day of the week is: Saturday");
    if(dayOfWeek==7) puts("The day of the week is: Sunday");
    return 0;
}
